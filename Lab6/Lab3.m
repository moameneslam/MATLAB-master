%find the square and fifth power of the numbers from 1 to 10
k = 1:10;  % Create an array of numbers from 1 to 10 and assign it to the variable 'k'
s = k.^2;  % Square each element in the array 'k' and assign the result to the variable 's'
l = k.^5;  % Raise each element in the array 'k' to the power of 5 and assign the result to the variable 'l'
disp(s);  % Display the values in the variable 's'
disp(l);  % Display the values in the variable 'l'